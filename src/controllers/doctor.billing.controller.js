const billModel = require('../models/bill.model');
const appointmentModel = require('../models/appointment.model');
const doctorModel = require('../models/doctor.model');

/*
 * - Generate Bill
 * - POST /api/doctor/billing
 */
async function generateBill(req, res) {
    try {
        const { appointmentId, items } = req.body;

        if (!appointmentId || !items || items.length === 0) {
            return res.status(400).json({ message: 'appointmentId and items are required' });
        }

        const appointment = await appointmentModel.findOne({
            _id: appointmentId,
            doctor: req.user._id,
            status: 'COMPLETED'
        });

        if (!appointment) {
            return res.status(404).json({ message: 'Completed appointment not found' });
        }

        const alreadyExists = await billModel.findOne({ appointment: appointmentId });

        if (alreadyExists) {
            return res.status(422).json({ message: 'Bill already generated for this appointment' });
        }

        const totalAmount = items.reduce((sum, item) => sum + item.amount, 0);

        const bill = await billModel.create({
            patient: appointment.patient,
            appointment: appointmentId,
            items,
            totalAmount
        });

        return res.status(201).json({
            message: 'Bill generated successfully',
            bill
        });

    } catch (error) {
        console.log(error);
        return res.status(500).json({ message: 'Internal server error' });
    }
}

/*
 * - Get Bills Generated by Doctor's Appointments
 * - GET /api/doctor/billing
 */
async function getDoctorBills(req, res) {
    try {
        const appointments = await appointmentModel.find({ doctor: req.user._id }, '_id');
        const appointmentIds = appointments.map(a => a._id);

        const bills = await billModel.find({ appointment: { $in: appointmentIds } })
            .populate('patient')
            .populate('appointment', 'date timeSlot reason')
            .sort({ createdAt: -1 });

        return res.status(200).json({ bills });

    } catch (error) {
        console.log(error);
        return res.status(500).json({ message: 'Internal server error' });
    }
}

module.exports = {
    generateBill,
    getDoctorBills
};